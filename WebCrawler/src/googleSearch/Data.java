package googleSearch;

// Class which will hold all the information each value in our data set will contain
// @Peter feel free to add any functionality you see fit!
public class Data {
	private String title;
	private String url;
	private String date;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
